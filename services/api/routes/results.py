"""
Results router.

Endpoints:
    GET /api/results/{task_id}              Get final result for a task's latest run
    GET /api/results/{task_id}/export       Export result as Markdown
"""

from __future__ import annotations

from fastapi import APIRouter, Depends, HTTPException
from fastapi.responses import PlainTextResponse
from sqlalchemy.orm import Session

from ..database import get_db
from ..models import Result, Run
from ..schemas import ResultResponse

router = APIRouter(prefix="/api/results", tags=["results"])


@router.get("/{task_id}", response_model=ResultResponse)
def get_result(task_id: str, db: Session = Depends(get_db)):
    """Get the final result for the latest completed run of a task."""
    run = (
        db.query(Run)
        .filter(Run.task_id == task_id, Run.status == "completed")
        .order_by(Run.completed_at.desc())
        .first()
    )
    if not run:
        raise HTTPException(status_code=404, detail="No completed run found for this task")

    result = db.query(Result).filter(Result.run_id == run.id).first()
    if not result:
        raise HTTPException(status_code=404, detail="Result not yet available")

    return result.to_dict()


@router.get("/{task_id}/export", response_class=PlainTextResponse)
def export_result(task_id: str, db: Session = Depends(get_db)):
    """Export the result as a Markdown document."""
    run = (
        db.query(Run)
        .filter(Run.task_id == task_id, Run.status == "completed")
        .order_by(Run.completed_at.desc())
        .first()
    )
    if not run:
        raise HTTPException(status_code=404, detail="No completed run found for this task")

    result = db.query(Result).filter(Result.run_id == run.id).first()
    if not result:
        raise HTTPException(status_code=404, detail="Result not yet available")

    md = _to_markdown(result)
    return PlainTextResponse(content=md, media_type="text/markdown")


def _to_markdown(result: Result) -> str:
    stats = result.stats
    lines = [
        f"# Research Result",
        f"",
        f"**Run ID**: `{result.run_id}`  ",
        f"**Best Score**: `{result.best_score:.4f}`  ",
        f"**Generated**: {result.created_at.isoformat() if result.created_at else 'unknown'}",
        f"",
        f"---",
        f"",
        f"## Metrics",
        f"",
        f"| Metric | Value |",
        f"|--------|-------|",
    ]
    for k, v in stats.items():
        lines.append(f"| {k} | {v} |")

    lines += [
        f"",
        f"---",
        f"",
        f"## Best Candidate: {result.best_node_name}",
        f"",
        f"**Motivation**",
        f"",
        result.best_node_motivation,
        f"",
        f"**Code**",
        f"",
        f"```python",
        result.best_node_code,
        f"```",
        f"",
        f"**Analysis**",
        f"",
        result.best_node_analysis,
        f"",
        f"---",
        f"",
        f"*Generated by R U Socrates — the transparent research engine.*",
    ]
    return "\n".join(lines)
